            <section class="7u 12u$(small)">
                        <p>
                    Lucrarea de tineret din cadrul bisericii „Vestea Bună” a început în urma cu aproape două decenii. În tot acest timp, preocuparea noastră a fost să dobândim caractere hristice, să întrupăm valorile Împărăției Domnului nostru și să trăim o viață vrednică de Evanghelia lui Isus Hristos în orașul nostru. 
                    </p>
                    <p>Pentru îndeplinirea acestor obiective duhovnicești, în urmă cu aproape trei ani, am constatat că întâlnirile pe grupe de casă sunt mai benefice decât alte forme de întâlniri. Prin urmare, am începută să ne întâlnim în grupe de casă în cadrul cărora am reușit să formăm un cadru de cunoaștere, de relaționare, în care fiecare tânăr să-și poată manifesta darurile și să se maturizeze în relațiile personale cu ceilalți și în relația cu Hristos. 
                    </p>
                    <p>Pentru a râmâne în părtășie cu toți tinerii din toate grupurile, în fiecare lună avem câte o întâlnire de tineret comună în care invităm toți tinerii să ne închinăm împreună, să ascultăm proclamarea Cuvântului și să creștem duhovnicește în dragostea lui Hristos. De asemenea, la aceste întâlniri încurajăm tinerii să-și invite colegii de școală, de muncă, prietenii, altfel încât și alții să-l cunoască pe Hristos și să se implice în activitățile Bisericii.
                    </p>
