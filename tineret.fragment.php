            <section class="12u 12u$(small)">
                <p>
                    <span class="image right">
                        <a href="[asset url='images/_/grupa_copii_scoala_duminicala.jpg']"><img src="[asset url='images/_/grupa_copii_scoala_duminicala.jpg']" alt="Scoala Duminicala Biserica Vestea Buna Bucuresti"/></a>
</span>
                Datoria stabilită de Dumnezeu în sarcina părinților este de a-şi creşte copiii în învățătura creştină, însă un ajutor venit în întâmpinarea acestei îndatoriri este şcoala duminicală. Pentru că şi copiii au nevoie de hrană spirituală, potrivită vârstei lor, am creat trei grupe ce se întrunesc în timpul programului de duminică dimineață.
</p>
<p>
    <h4>Organizare</h4>
Fiecare grupă este coordonată de câte o echipă de cel puțin trei învățători, iar grupele sunt împărțite pe categorii de vârstă după cum urmează:
</p>
<ul>
    <li>
Grupa mică (3-6 ani) aferentă preşcolarilor
</li><li>
Grupa mijlocie (7-11) aferentă claselor primare
</li><li>
Grupa mare (12-15) aferentă claselor gimnaziale
</li>
</ul>
<h4>Obiective</h4>
<p>
Dintre obiectivele şcolii duminicale, amintim:
</p>
<ul><li>
Explicarea Cuvântului lui Dumnezeu pe înțelesul copiilor, pentru întâlnirea lor personală cu Hristos şi creşterea lor spirituală
</li><li>
De a învăța principiile şi adevărurile biblice, adaptate fiecărei categorii de vârstă, prin lecții biblice
</li><li>
De a contribui la dezvoltarea copiilor şi asimilarea practică a învățăturilor creştine prin jocuri şi activități
</li><li>
De a-i ajuta pe copiii bisericii să clădească relații cu copii creştini de aceeaşi vârstă
</li>
</ul>
<h5>
Ne dorim transformarea vieții copiilor, cu impact în generațiile viitoare.
</h5>

