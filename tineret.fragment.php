                    <section class="6u$ 12u$(small)">
                        <p>Departamentul „Adolescenții Vestea Bună” este locul în care s-a format o comunitate a liceenilor din biserică unde încurajăm și echipăm tinerii pentru a discerne locul lor în împărăția lui Dumnezeu. AVB a apărut în anul 2013 ca răspuns la nevoia bisericii de a se ocupa de tinerii care erau prea mari pentru a merge la grupele de școală duminicală și prea mici pentru a participa la întâlnirile de tineret.
                        </p>
                        <h4>Ca obiective avem:</h4>
                        <ul>
                        <li>Să facilităm trecerea de la un set de cunoștințe biblice căpătate anterior la experimentarea personală a credințelor și valorilor creștine</li>
                        <li>Să oferim un cadru de mentorare și suport pentru tineri</li>
                        <li>Să cultivăm practicarea disciplinelor spirituale</li>
                        <li>Să oferim un cadru în care adolescenții să își șlefuiască diferitele daruri în vederea slujirii bisericii precum: îndemn la rugăciune, cântare, implicarea în pregătirea diferitelor evenimente, ajutorare socială etc.</li>
                        <li>Să facilităm un loc de socializare și cunoaștere dincolo de programele obișnuite ale bisericii</li>
                        <li>Să asigurăm trecerea de la Școala Duminicală spre Tineret a fiecărui tânăr</li>
